#include <stdio.h>

// questao adiada

int main(void){
    
    int m[4][4];
    int i, j;

    printf("\nMatriz Completa: \n\n");
    for (i = 0; i < 4; i++){

        for (j = 0; j < 4; j++){

            m[i][j] = j;

        }

    }

    // matriz triangular

    printf("\nMatriz Completa: \n\n");return 0;
}